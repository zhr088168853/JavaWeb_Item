package manager;

public class ManInfo {
    private String man_name;
    private String man_user;
    private String man_pwd;
    private String man_age;

    public ManInfo() {
    }

    public String getManname() {
        return man_name;
    }

    public void setManname(String man_name) {
        this.man_name = man_name;
    }

    public String getManuser() {
        return man_user;
    }

    public void setManuser(String man_user) {
        this.man_user = man_user;
    }

    public String getManpwd() {
        return man_pwd;
    }

    public void setManpwd(String man_pwd) {
        this.man_pwd = man_pwd;
    }

    public String getManage() {
        return man_age;
    }

    public void setManage(String man_age) {
        this.man_age = man_age;
    }
}
